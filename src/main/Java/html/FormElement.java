public class FormElement {
    public String type;
    public String name;
    public String text;
    public String htmlClass;

    public FormElement(String type, String name, String text, String htmlClass) {
        this.type = type;
        this.name = name;
        this.text = text;
        this.htmlClass = htmlClass;
    }
}
